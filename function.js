// addition
function add(num1, num2){
    sum = num1 + num2;
    return sum;
}
var result1 = add(10, 20);
console.log(result1);

var result2 = add(20, 30);
console.log(result2)

// multiplication
function multiply(num1, num2){
    multiplication = num1 * num2;
    return multiplication;
}
var result3 = multiply(9, 9);
console.log(result3)


// substraction
function substract(num1, num2){
    substraction = num1 - num2;
    return substraction;
}
var result4 = substract(50, 10);
console.log(result4);

// division
function divide(num1, num2){
    division = num1 / num2;
    return division;
}
var result5 = divide(40, 4);
console.log(result5)